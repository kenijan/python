

# Comparison_Operators or Relational_Operators

# 1. Equal (==)
a = 5
b = 5
print(f'1. Equal (==)            {a} == {b}:', a == b)  # True

# 2. Not Equal (!=)
a = 5
b = 3
print(f'2. Not Equal (!=)        {a} != {b}:', a != b)  # True

# 3. Greater Than (>)
a = 5
b = 3
print(f'3. Greater Than (>)      {a} > {b}:', a > b)  # True

# 4. Less Than (<)
a = 5
b = 10
print(f'4. Less Than (<)         {a} < {b}:', a < b)  # True

# 5. Greater Than or Equal To (>=)
a = 5
b = 5
print(f'5. < or Equal To (>=)    {a} >= {b}:', a >= b)  # True

# 6. Less Than or Equal To (<=)
a = 5
b = 7
print(f'6. > or Equal To (<=)    {a} <= {b}:', a <= b)  # True
