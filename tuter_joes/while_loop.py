

# while_loop
"""
while condition:
    # Code to execute while the condition is True

"""
count = 0

while count < 5:
    print(f"Count is {count}")
    count += 1  # Increment count by 1
'''

How it works:
    Initialize: count is set to 0.
    Check Condition: The while loop checks if count < 5. As long as this is True, it will run the code inside the loop.
    Execute Block: The loop prints count and then increments count by 1.
    Repeat or Stop: After each iteration, the loop checks the condition again:
    When count reaches 5, the condition count < 5 becomes False, so the loop stops.

'''
