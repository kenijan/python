# range
'''

Syntax: range(start, stop, step)
          start (optional): The starting value of the sequence (inclusive). If omitted, it defaults to 0.
          stop: The ending value of the sequence (exclusive), meaning the number just before the stop value is included.
          step (optional): The difference between each number in the sequence. If omitted, it defaults to 1.

'''


print(list(range(5)))
print(list(range(2,5)))
print(list(range(-1,5)))
print(list(range(0,10,2)))
print(list(range(10, 0, -2)))
